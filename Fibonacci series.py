num=int(input("Enter the limit:"))
num1=0
num2=1
number=0

if num<=0:
    print("Invalid Input:Positive number required!!!")
elif num==1:
    print("Fibonacci series upto",num,"is:",num1)
else:
    while number<num:
        print(num1)
        n=num1+num2
        num1=num2
        num2=n
        number+=1
